package com.itwillbs.funding.vo;

public class SupportVO {
	private int support_idx;
	private int member_idx;
	private String support_amount;
	private String support_public;
	public int getSupport_idx() {
		return support_idx;
	}
	public void setSupport_idx(int support_idx) {
		this.support_idx = support_idx;
	}
	public int getMember_idx() {
		return member_idx;
	}
	public void setMember_idx(int member_idx) {
		this.member_idx = member_idx;
	}
	public String getSupport_amount() {
		return support_amount;
	}
	public void setSupport_amount(String support_amount) {
		this.support_amount = support_amount;
	}
	public String getSupport_public() {
		return support_public;
	}
	public void setSupport_public(String support_public) {
		this.support_public = support_public;
	}
	
}
